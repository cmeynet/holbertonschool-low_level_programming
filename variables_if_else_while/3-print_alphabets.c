#include <stdio.h>
/**
 * main - Loop for print the alphabet in lowercase then in uppercase
 *
 * Return: the alphabet in lowercase follow by uppercase
 */
int main(void)
{
	char c;

	for (c = 'a'; c <= 'z'; c++)
	{
		putchar(c);
	}
	for (c = 'A'; c <= 'Z'; c++)
	{
		putchar(c);
	}
	putchar('\n');
	return (0);
}
